const languageNames: Record<Language, string> = {
  ar: "العربية",
  fr: "Français",
};

const I18nContext = createContext<I18nContextType | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    if (typeof window !== "undefined") {
      const stored = localStorage.getItem("shifa-lang") as Language;
      return stored === "ar" || stored === "fr" ? stored : "ar";
    }
    return "ar";
  });

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("shifa-lang", lang);
  }, []);

  const dir = language === "fr" ? "ltr" : "rtl";
  const isRTL = dir === "rtl";

  useEffect(() => {
    document.documentElement.dir = dir;
    document.documentElement.lang = language;
    if (isRTL) {
      document.documentElement.style.fontFamily = "var(--font-arabic)";
    } else {
      document.documentElement.style.fontFamily = "var(--font-sans)";
    }
  }, [language, dir, isRTL]);

  const t = useCallback(
    (key: string) => {
      return translations[language]?.[key] || translations.ar[key] || key;
    },
    [language]
  );

  return (
    <I18nContext.Provider value={{ language, setLanguage, t, dir, isRTL }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}

